#define F_CPU 8000000UL
#include<avr/io.h>
#include<util/delay.h>
#include<avr/interrupt.h>
#include"USARTcom.h"

//**************************************************************************************
//VARIABLES
//**************************************************************************************

uint8_t rData = 0;

//**************************************************************************************
//PROTOTIPOS
//**************************************************************************************
void IO_set(void);

//**************************************************************************************
//PROGRAMA
//**************************************************************************************
int main(void)
{
	IO_set();
	USART_BDset(9600,Async,0,8,1);
	USART_Rset(1,1);
	USART_Tset(0,0);
    while(1)
    {
        PORTB = rData;
    }
}


void IO_set(void){
	DDRB = 0xFF;
	return;
}

//**************************************************************************************
//INTERRUPCIONES
//**************************************************************************************

ISR(USART_RX_vect)
{
	rData = USART_RecInt();
}